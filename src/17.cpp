#include <iostream>
using namespace std;

int main() {
    int x = 5;
    double y = 10.5;
    float z = 2.7f;
    string s = "Hello, World!";
    bool b = true;

    cout << "Value of x: " << x << endl;
    cout << "Value of y: " << y << endl;
    cout << "Value of z: " << z << endl;
    cout << "Value of s: " << s << endl;
    cout << "Boolean value b: " << b << endl;

    return 0;
}
